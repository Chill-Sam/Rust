// addition function
pub fn add(a: f64, b: f64) -> f64
{
    return a + b; // returns answer
}

// subtraction function
pub fn sub(a: f64, b: f64) -> f64
{
    return a - b; // return answer
}

// multiplication function
pub fn mul(a: f64, b: f64) -> f64
{
    return a * b; // return answer
}

// divition function
pub fn div(a: f64, b: f64) -> f64
{
    return a / b; // return answer
}